export function getPhoneCode(country) {
    const entry = COUNTRY_PHONES.find((c) => c.country === country);
    return entry?.code || '';
}

export function getPhoneEntryByCountry(country) {
    return COUNTRY_PHONES.find((c) => c.country === country) || COUNTRY_PHONES[0];
}

export function getCountryOptions() {
    return COUNTRIES.map((c) => ({ value: c, label: c }));
}

export function getCityOptions(country) {
    const cities = CITIES_BY_COUNTRY[country];
    if (!cities) return [];
    return cities.map((c) => ({ value: c, label: c }));
}

export function filterOptions(options, query) {
    if (!query) return options;
    const q = query.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');
    return options.filter((o) => {
        const label = o.label.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');
        return label.includes(q);
    });
}
